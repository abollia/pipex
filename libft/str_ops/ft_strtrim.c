#include "../libft.h"

char	*ft_strtrim(const char *s1, const char *set)
{
	char	*res;
	size_t	start;
	size_t	end;

	if (!s1)
		return (NULL);
	if (!set)
		return (ft_strdup(s1));
	start = 0;
	end = ft_strlen(s1) - 1;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	while (s1[end] > 0 && ft_strrchr(set, s1[end]))
		end--;
	res = ft_substr(s1, start, (end - start + 1));
	return (res);
}
